void main() {
  // Using curly braces {}
  var numbers = {1, 2, 3, 4};
  print(numbers); // {1, 2, 3, 4}

  // Using Set constructor
  var names = <String>{'Eiman', 'Ali', 'Sara'};
  print(names); // {Eiman, Ali, Sara}

  // Empty set (must specify type)
  var emptySet = <int>{};
  print(emptySet); // {}
}
